package balhau.network;

/**
 * Classe com um conjunto de propriedades estáticas que representam as mensagens tipicamente utilizadas
 * na comunicação de redes.
 * @author balhau
 *
 */
public class Message {
	public static String CR="\r";
	public static String LF="\n";
	public static String CRLF=CR+LF;
	public static String EOL=CRLF+CRLF;
}
